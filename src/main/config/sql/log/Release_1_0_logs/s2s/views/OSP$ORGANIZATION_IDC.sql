
CREATE OR REPLACE VIEW OSP$ORGANIZATION_IDC  ( 
ORGANIZATION_ID, IDC_NUMBER, START_DATE, END_DATE, REQUESTED_DATE, IDC_RATE_TYPE_CODE, APPLICABLE_IDC_RATE, IDC_COMMENT, UPDATE_TIMESTAMP, UPDATE_USER 
)
AS select ORGANIZATION_ID, IDC_NUMBER, START_DATE, END_DATE, REQUESTED_DATE, IDC_RATE_TYPE_CODE, APPLICABLE_IDC_RATE, IDC_COMMENT, UPDATE_TIMESTAMP, UPDATE_USER
	from ORGANIZATION_IDC;
