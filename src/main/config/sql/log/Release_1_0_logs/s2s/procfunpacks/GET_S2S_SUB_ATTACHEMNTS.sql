CREATE OR REPLACE PROCEDURE GET_S2S_SUB_ATTACHEMNTS (
    AW_PROPOSAL_NUMBER IN  OSP$S2S_APP_ATTACHMENTS.PROPOSAL_NUMBER%TYPE,
    CUR_GENERIC     IN OUT RESULT_SETS.CUR_GENERIC )

IS
BEGIN

  OPEN CUR_GENERIC FOR
    SELECT

		  CONTENT_ID,
		  PROPOSAL_NUMBER,
		  HASH_CODE,
		  CONTENT_TYPE,
		  UPDATE_TIMESTAMP,
		  UPDATE_USER
    FROM
		OSP$S2S_APP_ATTACHMENTS SUB
    WHERE
      PROPOSAL_NUMBER = AW_PROPOSAL_NUMBER;
END;
/

