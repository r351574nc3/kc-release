CREATE OR REPLACE procedure dw_get_organization_detail
   ( as_organization_id IN osp$organization.organization_id%TYPE,
     cur_organization IN OUT result_sets.cur_generic) is

begin
open cur_organization for
SELECT OSP$ORGANIZATION.ORGANIZATION_ID,
         OSP$ORGANIZATION.ORGANIZATION_NAME,
         OSP$ORGANIZATION.CONTACT_ADDRESS_ID,
			'' CONTACT_ADDRESS_NAME,
			OSP$ORGANIZATION.ADDRESS,
         OSP$ORGANIZATION.CABLE_ADDRESS,
         OSP$ORGANIZATION.TELEX_NUMBER,
         OSP$ORGANIZATION.COUNTY,
         OSP$ORGANIZATION.CONGRESSIONAL_DISTRICT,
         OSP$ORGANIZATION.INCORPORATED_IN,
         OSP$ORGANIZATION.INCORPORATED_DATE,
         OSP$ORGANIZATION.NUMBER_OF_EMPLOYEES,
         OSP$ORGANIZATION.IRS_TAX_EXCEMPTION,
         OSP$ORGANIZATION.FEDRAL_EMPLOYER_ID,
         OSP$ORGANIZATION.MASS_TAX_EXCEMPT_NUM,
         OSP$ORGANIZATION.AGENCY_SYMBOL,
         OSP$ORGANIZATION.VENDOR_CODE,
         OSP$ORGANIZATION.COM_GOV_ENTITY_CODE,
         OSP$ORGANIZATION.MASS_EMPLOYEE_CLAIM,
         OSP$ORGANIZATION.DUNS_NUMBER,
         OSP$ORGANIZATION.DUNS_PLUS_FOUR_NUMBER,
         OSP$ORGANIZATION.DODAC_NUMBER,
         OSP$ORGANIZATION.CAGE_NUMBER,
         OSP$ORGANIZATION.HUMAN_SUB_ASSURANCE,
         OSP$ORGANIZATION.ANIMAL_WELFARE_ASSURANCE,
         OSP$ORGANIZATION.SCIENCE_MISCONDUCT_COMPL_DATE,
         OSP$ORGANIZATION.PHS_ACOUNT,
         OSP$ORGANIZATION.NSF_INSTITUTIONAL_CODE,
         OSP$ORGANIZATION.INDIRECT_COST_RATE_AGREEMENT,
         OSP$ORGANIZATION.COGNIZANT_AUDITOR,
			'' COGNIZANT_AUDITOR_NAME,
         OSP$ORGANIZATION.ONR_RESIDENT_REP,
			'' ONR_RESIDENT_REP_NAME,
         OSP$ORGANIZATION.UPDATE_TIMESTAMP,
         OSP$ORGANIZATION.UPDATE_USER
    FROM OSP$ORGANIZATION
	WHERE	 OSP$ORGANIZATION.ORGANIZATION_ID = as_organization_id;
end;
/

