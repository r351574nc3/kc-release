create or replace procedure GetBudgetJustification
   ( AW_PROPOSAL_NUMBER IN OSP$EPS_PROP_ABSTRACT.PROPOSAL_NUMBER%TYPE, 
     AW_VERSION_NUMBER IN OSP$BUDGET.VERSION_NUMBER%TYPE,
     cur_generic IN OUT result_sets.cur_generic) is 

begin 
	open cur_generic for 
	SELECT BUDGET_JUSTIFICATION 
   	  
	FROM 	 OSP$BUDGET_JUSTIFICATION, OSP$BUDGET 
   WHERE  OSP$BUDGET.PROPOSAL_NUMBER = AW_PROPOSAL_NUMBER
   AND    OSP$BUDGET.PROPOSAL_NUMBER = OSP$BUDGET_JUSTIFICATION.PROPOSAL_NUMBER
	AND    OSP$BUDGET.VERSION_NUMBER = OSP$BUDGET_JUSTIFICATION.VERSION_NUMBER
	AND	 OSP$BUDGET.VERSION_NUMBER = AW_VERSION_NUMBER;
end;

/




