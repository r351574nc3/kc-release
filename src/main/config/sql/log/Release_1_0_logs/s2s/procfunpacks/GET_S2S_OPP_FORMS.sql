CREATE OR REPLACE PROCEDURE GET_S2S_OPP_FORMS (
    AW_PROPOSAL_NUMBER IN  OSP$S2S_OPPORTUNITY.PROPOSAL_NUMBER%TYPE,
    CUR_GENERIC     IN OUT RESULT_SETS.CUR_GENERIC )

IS
BEGIN

  OPEN CUR_GENERIC FOR
    SELECT
		FRM.PROPOSAL_NUMBER,
		FRM.OPP_NAME_SPACE,
		FRM.FORM_NAME,
		FRM.MANDATORY,
		FRM.AVAILABLE,
		FRM.INCLUDE,
		FRM.UPDATE_TIMESTAMP,
		FRM.UPDATE_USER
    FROM
		OSP$S2S_OPP_FORMS FRM
    WHERE
      FRM.PROPOSAL_NUMBER = AW_PROPOSAL_NUMBER;
END;
/

