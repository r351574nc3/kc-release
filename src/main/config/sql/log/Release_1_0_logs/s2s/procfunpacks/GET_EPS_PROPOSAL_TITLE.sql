
  CREATE OR REPLACE PROCEDURE GET_EPS_PROPOSAL_TITLE 
    --This procedure returns the title of the Development Proposal record
  ( AV_PROPOSAL_NUMBER IN OSP$EPS_PROPOSAL.PROPOSAL_NUMBER%TYPE,
    as_title OUT OSP$EPS_PROPOSAL.TITLE%TYPE)
    IS
    BEGIN
    SELECT TITLE
    INTO as_title
    FROM OSP$EPS_PROPOSAL
    WHERE OSP$EPS_PROPOSAL.PROPOSAL_NUMBER = AV_PROPOSAL_NUMBER ;

	EXCEPTION

			 WHEN NO_DATA_FOUND THEN
			 	  as_title := '';
END ;
/
 
