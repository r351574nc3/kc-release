CREATE OR REPLACE procedure dw_get_cust_columns_for_module
   ( AW_MODULE_CODE IN OSP$CUSTOM_DATA_ELEMENT_USAGE.MODULE_CODE%TYPE,
	  cur_generic IN OUT result_sets.cur_generic) is

begin

/****************
fIX 239
addred order by clause
*****************/

open cur_generic for

  SELECT OSP$CUSTOM_DATA_ELEMENTS.COLUMN_NAME,
		  OSP$CUSTOM_DATA_ELEMENTS.COLUMN_LABEL,
		  OSP$CUSTOM_DATA_ELEMENTS.DATA_TYPE,
		  OSP$CUSTOM_DATA_ELEMENTS.DATA_LENGTH,
		  OSP$CUSTOM_DATA_ELEMENTS.DEFAULT_VALUE,
		  OSP$CUSTOM_DATA_ELEMENTS.HAS_LOOKUP,
		  OSP$CUSTOM_DATA_ELEMENTS.LOOKUP_WINDOW,
		  OSP$CUSTOM_DATA_ELEMENTS.LOOKUP_ARGUMENT,
		  OSP$CUSTOM_DATA_ELEMENT_USAGE.IS_REQUIRED,
  		  OSP$CUSTOM_DATA_ELEMENTS.GROUP_NAME -- Added for CoeusLite Enhancement
  FROM OSP$CUSTOM_DATA_ELEMENTS , OSP$CUSTOM_DATA_ELEMENT_USAGE
  WHERE OSP$CUSTOM_DATA_ELEMENT_USAGE.MODULE_CODE = AW_MODULE_CODE
		AND OSP$CUSTOM_DATA_ELEMENTS.COLUMN_NAME = OSP$CUSTOM_DATA_ELEMENT_USAGE.COLUMN_NAME
	ORDER by OSP$CUSTOM_DATA_ELEMENTS.COLUMN_LABEL;

end;
/

