CREATE OR REPLACE PROCEDURE GET_PROTO_CORRESP_TYPES_DESCR (
    AV_PROTO_CORRESP_TYPE_CODE IN  OSP$PROTO_CORRESP_TYPE.PROTO_CORRESP_TYPE_CODE%TYPE,
    CUR_GENERIC IN OUT RESULT_SETS.CUR_GENERIC )

IS
BEGIN
  OPEN CUR_GENERIC FOR
    SELECT
      OSP$PROTO_CORRESP_TYPE.DESCRIPTION
    FROM
      OSP$PROTO_CORRESP_TYPE
	  WHERE OSP$PROTO_CORRESP_TYPE.PROTO_CORRESP_TYPE_CODE = AV_PROTO_CORRESP_TYPE_CODE;

END;
/

