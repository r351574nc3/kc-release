create or replace FUNCTION FN_DEL_S2S_SUB_APPLICATION(
    AW_PROPOSAL_NUMBER IN  OSP$S2S_APPLICATION.PROPOSAL_NUMBER%TYPE)
	return number
IS
BEGIN
	 DELETE FROM OSP$S2S_APP_ATTACHMENTS WHERE PROPOSAL_NUMBER = AW_PROPOSAL_NUMBER;
	 DELETE FROM OSP$S2S_APPLICATION WHERE PROPOSAL_NUMBER = AW_PROPOSAL_NUMBER;
	 return 1;
END;
