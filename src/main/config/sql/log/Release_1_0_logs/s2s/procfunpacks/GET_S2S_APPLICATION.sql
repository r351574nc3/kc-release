CREATE OR REPLACE PROCEDURE GET_S2S_APPLICATION (
	AW_PROPOSAL_NUMBER 	 IN OSP$S2S_APPLICATION.PROPOSAL_NUMBER%TYPE,
    	CUR_GENERIC 	 IN OUT RESULT_SETS.CUR_GENERIC )
IS
BEGIN

  OPEN CUR_GENERIC FOR
    SELECT
		  PROPOSAL_NUMBER,
		  APPLICATION,
		  UPDATE_TIMESTAMP,
		  UPDATE_USER
    FROM
      	  OSP$S2S_APPLICATION
    WHERE
      	  PROPOSAL_NUMBER = AW_PROPOSAL_NUMBER;
END;
/

