CREATE OR REPLACE procedure dw_get_eps_prop_idc_rate
   (  AW_PROPOSAL_NUMBER IN OSP$EPS_PROP_IDC_RATE.PROPOSAL_NUMBER%TYPE,
		AW_VERSION_NUMBER IN OSP$EPS_PROP_IDC_RATE.VERSION_NUMBER%TYPE,
		cur_generic IN OUT result_sets.cur_generic) is

begin

open cur_generic for

  SELECT *
  FROM OSP$EPS_PROP_IDC_RATE
  WHERE 	PROPOSAL_NUMBER = AW_PROPOSAL_NUMBER
	AND   VERSION_NUMBER = AW_VERSION_NUMBER
	order by FISCAL_YEAR asc;

end;
/

