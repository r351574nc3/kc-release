CREATE TABLE OSP$PARAMETER 
   (	PARAMETER VARCHAR2(40 BYTE) NOT NULL ENABLE, 
	EFFECTIVE_DATE DATE NOT NULL ENABLE, 
	VALUE VARCHAR2(250 BYTE), 
	UPDATE_TIMESTAMP DATE NOT NULL ENABLE, 
	UPDATE_USER VARCHAR2(8 BYTE) NOT NULL ENABLE, 
	 CONSTRAINT PK_PARAMETER PRIMARY KEY (PARAMETER, EFFECTIVE_DATE) DISABLE
   );