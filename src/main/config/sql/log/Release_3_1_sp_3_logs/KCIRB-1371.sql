CREATE TABLE WATERMARK(	
    WATERMARK_ID NUMBER(12,0) NOT NULL ENABLE, 
    STATUS_CODE VARCHAR2(8 BYTE) NOT NULL ENABLE, 
    WATERMARK_TEXT VARCHAR2(100 BYTE) NOT NULL ENABLE, 
    WATERMARK_STATUS CHAR(1 BYTE) NOT NULL ENABLE, 
    FONT_SIZE VARCHAR2(5 BYTE), 
    FONT_COLOUR VARCHAR2(50 BYTE), 
    UPDATE_TIMESTAMP DATE NOT NULL ENABLE, 
    VER_NBR NUMBER(8,0) NOT NULL ENABLE, 
    OBJ_ID VARCHAR2(36 BYTE) NOT NULL ENABLE, 
    UPDATE_USER VARCHAR2(60 BYTE) NOT NULL ENABLE, 
    WATERMARK_TYPE VARCHAR2(20 BYTE), 
    IMAGE_FILE BLOB, 
    CONSTRAINT WATERMARK_PK PRIMARY KEY (WATERMARK_ID)
   );