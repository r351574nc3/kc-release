CREATE OR REPLACE VIEW OSP$AWARD_SCIENCE_CODE(MIT_AWARD_NUMBER, 
	SEQUENCE_NUMBER, 
	SPECIAL_REVIEW_NUMBER, 
	SPECIAL_REVIEW_CODE, 
	APPROVAL_TYPE_CODE, 
	PROTOCOL_NUMBER, 
	APPLICATION_DATE, 
	APPROVAL_DATE, 
	COMMENTS, 
	UPDATE_USER, 
	UPDATE_TIMESTAMP)
	AS SELECT 
	B.AWARD_NUMBER, 
	B.SEQUENCE_NUMBER, 
	A.SPECIAL_REVIEW_NUMBER, 
	A.SPECIAL_REVIEW_CODE, 
	A.APPROVAL_TYPE_CODE, 
	A.PROTOCOL_NUMBER, 
	A.APPLICATION_DATE, 
	A.APPROVAL_DATE, 
	A.COMMENTS, 
	A.UPDATE_USER, 
	A.UPDATE_TIMESTAMP 
	FROM AWARD_SPECIAL_REVIEW A,AWARD B
	where A.AWARD_ID=B.AWARD_ID;