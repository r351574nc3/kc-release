CREATE TABLE AWARD_APPROVED_SUBAWARDS ( 
	AWARD_APPROVED_SUBAWARD_ID	NUMBER(8,0),
	AWARD_ID					NUMBER,
	AWARD_NUMBER				VARCHAR2(10),
	SEQUENCE_NUMBER				NUMBER(8,0),
	ORGANIZATION_NAME			VARCHAR2(60),
	AMOUNT						NUMBER(12,2),
    UPDATE_TIMESTAMP       		DATE,
    UPDATE_USER            		VARCHAR2(60),
    VER_NBR 					NUMBER(8,0) DEFAULT 1,
	OBJ_ID 						VARCHAR2(36) DEFAULT SYS_GUID()
);
CREATE SEQUENCE SEQ_AWARD_APPROVED_SUBAWARD_ID START WITH 1 MAXVALUE 99999999 MINVALUE 1 NOCYCLE NOCACHE NOORDER;
alter table AWARD_APPROVED_SUBAWARDS ADD CONSTRAINT PK_AWARD_APPROVED_SUBAWARD PRIMARY KEY(AWARD_APPROVED_SUBAWARD_ID) ENABLE;
alter table AWARD_APPROVED_SUBAWARDS ADD CONSTRAINT U_AWARD_APPROVED_SUBAWARDS UNIQUE(AWARD_NUMBER,SEQUENCE_NUMBER,ORGANIZATION_NAME) ENABLE;
CREATE OR REPLACE VIEW OSP$AWARD_APPROVED_SUBCONTRACT AS SELECT 
	AWARD_NUMBER MIT_AWARD_NUMBER, 
	SEQUENCE_NUMBER SEQUENCE_NUMBER,
	ORGANIZATION_NAME SUBCONTRACTOR_NAME,
	AMOUNT AMOUNT,
	UPDATE_TIMESTAMP UPDATE_TIMESTAMP, 
	UPDATE_USER UPDATE_USER
FROM AWARD_APPROVED_SUBAWARDS;