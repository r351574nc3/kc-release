CREATE VIEW OSP$PROTOCOL_SPECIAL_REVIEW
AS SELECT PROTOCOL_NUMBER,  
          0 as SEQUENCE_NUMBER, 
          SPECIAL_REVIEW_NUMBER,
          SPECIAL_REVIEW_CODE, 
          APPROVAL_TYPE_CODE, 
          null as SP_REV_PROTOCOL_NUMBER,
          APPLICATION_DATE, 
          APPROVAL_DATE, 
          COMMENTS,
          UPDATE_TIMESTAMP, 
          UPDATE_USER
FROM PROTOCOL_SPECIAL_REVIEW;

commit;
