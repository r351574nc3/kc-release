CREATE TABLE AWARD_FUNDING_PROPOSALS (
   AWARD_FUNDING_PROPOSAL_ID number(22) NOT NULL,
   AWARD_ID number(22) NOT NULL,
   PROPOSAL_ID number(22) NOT NULL,
   UPDATE_TIMESTAMP timestamp NOT NULL,
   UPDATE_USER varchar2(60) NOT NULL,
   VER_NBR number(8) DEFAULT 1 NOT NULL,
   OBJ_ID varchar2(36) DEFAULT SYS_GUID() NOT NULL,

   CONSTRAINT PK_AWD_FUND_PROP PRIMARY KEY ( AWARD_FUNDING_PROPOSAL_ID ) ENABLE,
   CONSTRAINT U_AWD_FUND_PROP UNIQUE ( AWARD_ID, PROPOSAL_ID ) ENABLE,
   CONSTRAINT FK_AWD_FUND_PROP_AWARD FOREIGN KEY ( AWARD_ID ) REFERENCES AWARD ( AWARD_ID ) ENABLE,
   CONSTRAINT FK_AWD_FUND_PROP_PROPOSAL FOREIGN KEY ( PROPOSAL_ID ) REFERENCES PROPOSAL ( PROPOSAL_ID ) ENABLE
);