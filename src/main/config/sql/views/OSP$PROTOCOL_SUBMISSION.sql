CREATE or REPLACE VIEW OSP$PROTOCOL_SUBMISSION AS SELECT
    PROTOCOL_NUMBER,
    SEQUENCE_NUMBER,
    SUBMISSION_NUMBER,
    SCHEDULE_ID,
    COMMITTEE_ID,
    SUBMISSION_TYPE_CODE,
    SUBMISSION_TYPE_QUAL_CODE,
    PROTOCOL_REVIEW_TYPE_CODE
    SUBMISSION_STATUS_CODE,
    SUBMISSION_DATE,
    COMMENTS,
    YES_VOTE_COUNT,
    NO_VOTE_COUNT,
    ABSTAINER_COUNT,
    VOTING_COMMENTS,
    UPDATE_TIMESTAMP,
    UPDATE_USER
FROM PROTOCOL_SUBMISSION;
